package com.sabekur2017.calculatortddexample

class Calculator {
    fun sum(x: Double, y: Double): Double {
        return x + y
    }

    fun subtract(x: Double, y: Double): Double {
        return x - y
    }

    fun multiply(x: Double, y: Double): Double {
        return x * y
    }

    fun divide(x: Double, y: Double): Double {
        return x / y
    }
}